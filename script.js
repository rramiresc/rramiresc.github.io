    function getSavedData() {
        try {
            const data = localStorage.getItem('formData');
            return data ? JSON.parse(data) : [];
        } catch (e) {
            console.error("Erro ao ler dados do localStorage:", e);
            return [];
        }
    }

    function saveFormData(data) {
        try {
            const savedData = getSavedData();
            savedData.push(data);
            localStorage.setItem('formData', JSON.stringify(savedData));
            updateFormCount();
        } catch (e) {
            console.error("Erro ao salvar dados no localStorage:", e);
            alert("Erro ao salvar o formulário. Por favor, tente novamente.");
        }
    }

    function updateFormCount() {
        const savedData = getSavedData();
        formCountSpan.textContent = `Formulários Enviados: ${savedData.length}`;
    }

    function calculateTotalScore() {
        let totalScore = 0;
        document.querySelectorAll('.question-group').forEach((group) => {
            const ratingOptionsDiv = group.querySelector('.rating-options');
            if (ratingOptionsDiv) {
                const maxWeight = parseInt(ratingOptionsDiv.dataset.weight);
                const selectedRadio = ratingOptionsDiv.querySelector('input[type="radio"]:checked');

                if (selectedRadio) {
                    const selectedValue = parseInt(selectedRadio.value);

                    if (selectedValue === 5) {
                        totalScore += maxWeight;
                    } else if (selectedValue === 0) {
                        totalScore += 0;
                    } else {
                        totalScore += (selectedValue / 5) * maxWeight;
                    }
                }
            }
        });
        totalScoreSpan.textContent = `Nota: ${totalScore.toFixed(2)} / 50`;
    }

    function startTimer() {
        // O timer só deve iniciar se não estiver rodando E se startTime for nulo.
        // Se startTime for nulo, significa que é um novo preenchimento (ou acabou de limpar/enviar).
        if (!isTimerRunning && startTime === null) {
            startTime = new Date(); // Define o novo ponto de partida
            timerInterval = setInterval(updateTimer, 1000);
            isTimerRunning = true;
        }
    }

    function updateTimer() {
        if (startTime) {
            const now = new Date();
            const elapsed = now - startTime;
            const hours = Math.floor(elapsed / (1000 * 60 * 60));
            const minutes = Math.floor((elapsed % (1000 * 60 * 60)) / (1000 * 60));
            const seconds = Math.floor((elapsed % (1000 * 60)) / 1000);

            const format = (num) => num < 10 ? '0' + num : num;
            timerSpan.textContent = `${format(hours)}:${format(minutes)}:${format(seconds)}`;
        }
    }

    function stopTimer() {
        clearInterval(timerInterval);
        isTimerRunning = false;
        if (startTime) {
            const endTime = new Date();
            return Math.floor((endTime - startTime) / 1000);
        }
        return 0;
    }

    // Função de reset aprimorada: Para o timer, zera o display e reinicia o estado para 'pronto para iniciar'
    function resetTimer() {
        stopTimer(); // Para qualquer timer que esteja rodando
        startTime = null; // Zera o ponto de partida para o próximo início
        isTimerRunning = false; // Garante que o estado seja 'não rodando'
        timerSpan.textContent = '00:00:00'; // Zera o display
    }

